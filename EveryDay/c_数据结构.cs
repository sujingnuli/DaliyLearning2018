二：算法
	健壮性：有较强的容错能力。当输入不合法数据时，算法要做适当的处理。不至于引起严重的后果。
		    考虑到 边界和异常情况，并对边界和异常做妥善处理。尽可能没有意外发生。
	空间复杂度 Space Complexity.
			算法在 运行过程中 临时 占用的存储空间的大小。
			包括：	局部变量所占 存储空间
					系统为实现递归 所使用的 堆栈占用的 存储空间。
	时间复杂度 Time Complexity 
			算法的运行时间 与 问题规模的对应关系。
			O(1),O(n^2),
			T(n)=o(1),T(n)=O(n^2)
